#Variables
a = 15
x = 2
y = 10
z = 7

#Adición
suma = x + y
print(suma)

#Resta
resta = y - x
print(resta)

#Multiplicación
multiplicacion = x * y
print(multiplicacion)

#Division
division = z / x
print(division)

#Modulo / residuo de la división
modulo = y % z
print(modulo)

#Exponentes
exponente = x ** y
print(exponente)

#Redondeo al número más cercano
redondeo = a // 15
print(redondeo)
